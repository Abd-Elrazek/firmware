#pragma once

#include <cstddef>

namespace particle {

// Base abstract class for a random access input file
class InputFile {
public:
    virtual ~InputFile() = default;

    virtual int read(char* data, size_t size, size_t offs) = 0;
    virtual int size() const = 0;
};

} // particle
