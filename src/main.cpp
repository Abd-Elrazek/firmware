/* Private functions ---------------------------------------------------------*/
static void Bootloader_Patch_Update(void)
{
  extern volatile FLASH_Status FLASHStatus;
  extern uint32_t EraseCounter;
  extern uint32_t Internal_Flash_Address;
  extern uint32_t Internal_Flash_Data;

  FLASHStatus = FLASH_COMPLETE;

  if(Compute_CRC32((unsigned char *)USB_DFU_ADDRESS, bootloader_bin_len) == bootloader_bin_crc32)
  {
    /* Bootloader patch already applied so no need to continue further */
    return;
  }

  LED_SetRGBColor(RGB_COLOR_YELLOW);
  LED_On(LED_RGB);

  FLASH_WriteProtection_Disable(BOOTLOADER_FLASH_PAGES);

  /* Unlock the Flash Program Erase Controller */
  FLASH_Unlock();

  /* Clear All pending flags */
  FLASH_ClearFlag(FLASH_FLAG_EOP | FLASH_FLAG_PGERR | FLASH_FLAG_WRPRTERR);

  /* Erase the Internal Flash pages */
  for (EraseCounter = 0; (EraseCounter < 16) && (FLASHStatus == FLASH_COMPLETE); EraseCounter++)
  {
    FLASHStatus = FLASH_ErasePage(USB_DFU_ADDRESS + (INTERNAL_FLASH_PAGE_SIZE * EraseCounter));
  }

  Internal_Flash_Address = USB_DFU_ADDRESS;
  uint32_t count = bootloader_bin_len >> 2;
  uint32_t index = 0;
  SPARK_FLASH_UPDATE = 1;

  /* Program Internal Flash Bank1 */
  while ((FLASHStatus == FLASH_COMPLETE) && count--)
  {
    /* Program Word to Internal Flash memory */
    Internal_Flash_Data = (uint32_t)(bootloader_bin[index]
                                  | (bootloader_bin[index + 1] << 8)
                                  | (bootloader_bin[index + 2] << 16)
                                  | (bootloader_bin[index + 3] << 24));
    FLASHStatus = FLASH_ProgramWord(Internal_Flash_Address, Internal_Flash_Data);
    Internal_Flash_Address += 4;
    index += 4;
    LED_Toggle(LED_RGB);
  }

  /* Locks the FLASH Program Erase Controller */
  FLASH_Lock();

  FLASH_WriteProtection_Enable(BOOTLOADER_FLASH_PAGES);
}

/*******************************************************************************
 * Function Name  : SparkCoreConfig.
 * Description    : Called in startup routine, before calling C++ constructors.
 * Input          : None.
 * Output         : None.
 * Return         : None.
 *******************************************************************************/
extern "C" void SparkCoreConfig(void)
{
        DECLARE_SYS_HEALTH(ENTERED_SparkCoreConfig);
#ifdef DFU_BUILD_ENABLE
	/* Set the Vector Table(VT) base location at 0x5000 */
	NVIC_SetVectorTable(NVIC_VectTab_FLASH, 0x5000);

	USE_SYSTEM_FLAGS = 1;
#endif

#ifdef SWD_JTAG_DISABLE
	/* Disable the Serial Wire JTAG Debug Port SWJ-DP */
	GPIO_PinRemapConfig(GPIO_Remap_SWJ_Disable, ENABLE);
#endif

	Set_System();

	SysTick_Configuration();

	/* Enable CRC clock */
	RCC_AHBPeriphClockCmd(RCC_AHBPeriph_CRC, ENABLE);

        Bootloader_Patch_Update();

#if !defined (RGB_NOTIFICATIONS_ON)	&& defined (RGB_NOTIFICATIONS_OFF)
	LED_RGB_OVERRIDE = 1;
#endif

	LED_SetRGBColor(RGB_COLOR_WHITE);
	LED_On(LED_RGB);
	SPARK_LED_FADE = 1;

#if defined (SPARK_RTC_ENABLE)
	RTC_Configuration();
#endif

#ifdef IWDG_RESET_ENABLE
	// ToDo this needs rework for new bootloader
	/* Check if the system has resumed from IWDG reset */
	if (RCC_GetFlagStatus(RCC_FLAG_IWDGRST) != RESET)
	{
		/* IWDGRST flag set */
		IWDG_SYSTEM_RESET = 1;

		/* Clear reset flags */
		RCC_ClearFlag();
	}

	/* We are duplicating the IWDG call here for compatibility with old bootloader */
	/* Set IWDG Timeout to 3 secs */
	IWDG_Reset_Enable(3 * TIMING_IWDG_RELOAD);
#endif

#ifdef DFU_BUILD_ENABLE
	Load_SystemFlags();
#endif

#ifdef SPARK_SFLASH_ENABLE
	sFLASH_Init();
#endif

#ifdef SPARK_WLAN_ENABLE
	/* Start Spark Wlan and connect to Wifi Router by default */
	SPARK_WLAN_SETUP = 1;

	/* Connect to Spark Cloud by default */
	SPARK_CLOUD_CONNECT = 1;
#endif
}

